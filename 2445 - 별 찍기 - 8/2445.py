"""
예제 입력 1 
5
예제 출력 1 
*        *
**      **
***    ***
****  ****
**********
****  ****
***    ***
**      **
*        *
"""
N = int(input())
for i in range(N):
    print('*' * (i+1) + ' ' * (2*N-2-2*i) + '*' * (i+1))
for i in range(N-1):
    print('*' * ((N-1)-i) + ' ' * (2*i+2) + '*' * ((N-1)-i))
